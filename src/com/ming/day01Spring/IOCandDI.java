package com.ming.day01Spring;

public class IOCandDI {
	/*IOC(控制反转):将我们创建对象的方式反转，
	 * 	以前对象的创建是由我们自己维护，包括依赖
	 * 使用spring,对象的创建以及依赖关系可以由spring完成创建和注入
	 * 控制反转就是反转了对象的创建方式，从我们自己创建反转给了程序(Spring)
	 */
	/*DI(dependency inJection依赖注入):
	 * 实现IOC的思想，DI作支持
	 * 注入方式:
	 * 		值类型注入:八大基本类型
	 * 		引用类性注入:将依赖对象注入
	 * */
}
